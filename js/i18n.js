// Bengali-Western numeral helper
const BN_DIGITS = ['০','১','২','৩','৪','৫','৬','৭','৮','৯'];
function toBnNum(str){ return String(str).replace(/[0-9]/g, d => BN_DIGITS[d]); }

const I18nStore = {
  lang: localStorage.getItem('ab_lang') || 'en',
  get(key){ return (I18N[this.lang] && I18N[this.lang][key]) ?? I18N.en[key] ?? key; },
  set(lang){ this.lang = lang; localStorage.setItem('ab_lang', lang); this.apply(); },
  toggle(){ this.set(this.lang === 'en' ? 'bn' : 'en'); },
  fmtPrice(n){
    const s = '৳' + Number(n).toLocaleString('en-US');
    return this.lang === 'bn' ? toBnNum(s) : s;
  },
  apply(){
    document.documentElement.lang = this.lang;
    document.documentElement.setAttribute('data-lang', this.lang);
    document.querySelectorAll('[data-i18n]').forEach(el => {
      el.innerHTML = this.get(el.getAttribute('data-i18n'));
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      el.setAttribute('placeholder', this.get(el.getAttribute('data-i18n-ph')));
    });
    document.querySelectorAll('[data-price]').forEach(el => {
      el.textContent = this.fmtPrice(el.getAttribute('data-price'));
    });
    document.dispatchEvent(new CustomEvent('langchange', { detail: { lang: this.lang } }));
  }
};
